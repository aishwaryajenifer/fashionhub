package com.example.backend.model;

public class BodyShapeRequest {
    private double height;
    private double hipSize;
    private double shoulderLength;
    private double waistSize;
    private double bustSize;
    public double getHeight() {
        return height;
    }
    public void setHeight(double height) {
        this.height = height;
    }
    public double getHipSize() {
        return hipSize;
    }
    public void setHipSize(double hipSize) {
        this.hipSize = hipSize;
    }
    public double getShoulderLength() {
        return shoulderLength;
    }
    public void setShoulderLength(double shoulderLength) {
        this.shoulderLength = shoulderLength;
    }
    public double getWaistSize() {
        return waistSize;
    }
    public void setWaistSize(double waistSize) {
        this.waistSize = waistSize;
    }
    public double getBustSize() {
        return bustSize;
    }
    public void setBustSize(double bustSize) {
        this.bustSize = bustSize;
    }

    
}

